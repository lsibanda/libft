#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*dest;
	const char	*sorc;

	dest = dst;
	sorc = src;
	while (n--)
		*dest++ = *sorc++;
	return (dst);
}
