#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	i;

	i = 0;
	while (s1 && s2 && n > 0)
	{
		if (s1[i] == s2[i])
			n--;
		else
			return (0);
		i++;
	}
	return (1);
}
